# 2.13: Crea un dizionario con i nomi degli studenti e i loro voti, poi calcola la media dei voti.
studenti = {"Alice": 8, "Claudio": 7, "Giuseppe": 9, "Daniele": 4}
media_voti = sum(studenti.values()) / len(studenti)
print(f"La media dei voti è {media_voti}.")

